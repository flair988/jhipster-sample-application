import axios from 'axios';

import { IProductTaxmonomy } from '@/shared/model/product-taxmonomy.model';

const baseApiUrl = 'api/product-taxmonomies';

export default class ProductTaxmonomyService {
  public find(id: number): Promise<IProductTaxmonomy> {
    return new Promise<IProductTaxmonomy>((resolve, reject) => {
      axios
        .get(`${baseApiUrl}/${id}`)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public retrieve(): Promise<any> {
    return new Promise<any>((resolve, reject) => {
      axios
        .get(baseApiUrl)
        .then(res => {
          resolve(res);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public delete(id: number): Promise<any> {
    return new Promise<any>((resolve, reject) => {
      axios
        .delete(`${baseApiUrl}/${id}`)
        .then(res => {
          resolve(res);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public create(entity: IProductTaxmonomy): Promise<IProductTaxmonomy> {
    return new Promise<IProductTaxmonomy>((resolve, reject) => {
      axios
        .post(`${baseApiUrl}`, entity)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public update(entity: IProductTaxmonomy): Promise<IProductTaxmonomy> {
    return new Promise<IProductTaxmonomy>((resolve, reject) => {
      axios
        .put(`${baseApiUrl}/${entity.id}`, entity)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public partialUpdate(entity: IProductTaxmonomy): Promise<IProductTaxmonomy> {
    return new Promise<IProductTaxmonomy>((resolve, reject) => {
      axios
        .patch(`${baseApiUrl}/${entity.id}`, entity)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }
}
